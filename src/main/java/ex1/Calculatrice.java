package ex1;

/**
 * Cette classe représente une calculatrice pour effectuer des opérations mathématiques simples.
 */
public class Calculatrice {

    /**
     * Cette méthode additionne deux entiers.
     *
     * @param a Le premier entier.
     * @param b Le deuxième entier.
     * @return La somme des deux entiers.
     */
    public int additionner(int a, int b) {
        return a + b;
    }

    /**
     * Cette méthode soustrait un entier d'un autre.
     *
     * @param a Le premier entier.
     * @param b Le deuxième entier.
     * @return La différence entre le premier entier et le deuxième entier.
     */
    public int soustraire(int a, int b) {
        return a - b;
    }
}
